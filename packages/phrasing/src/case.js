/**
 * @param {string} word
 * @returns {string}
 */
export const capitalize = word => word[0].toUpperCase() + word.substring(1).toLowerCase()

/**
 * @param {string} word
 * @returns {string}
 */
export const toUpper = word => word.toUpperCase()

/**
 * @param {string} word
 * @returns {string}
 */
export const toLower = word => word.toLowerCase()

